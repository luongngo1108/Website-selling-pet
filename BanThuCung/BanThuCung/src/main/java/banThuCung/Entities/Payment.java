package banThuCung.Entities;

public class Payment {
	private String pay_id;
	private String name;
	public Payment() {
		super();
	}
	public String getPay_id() {
		return pay_id;
	}
	public void setPay_id(String pay_id) {
		this.pay_id = pay_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
