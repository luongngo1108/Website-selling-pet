package banThuCung.Entities;

public class Categories {
	private String cat_id;
	private String name;
	
	public Categories() {
		super();
	}
	
	public String getCat_id() {
		return cat_id;
	}
	
	public void setCat_id(String cat_id) {
		this.cat_id = cat_id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
